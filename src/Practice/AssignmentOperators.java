package Practice;

public class AssignmentOperators {
    public static void main(String[] args) {

        int a =6, b=8;
        // Assignment operators
        System.out.println("Assignment operators : ");
        System.out.println("+=: " + (a += 5));
        System.out.println("-=: " + (a -= 5));
        System.out.println("*=: " + (a *= 5));
        System.out.println("/=: " + (a /= 5));
        System.out.println("%=: " + (a %= 5));
        System.out.println("aaa: " + a);
    }
}
