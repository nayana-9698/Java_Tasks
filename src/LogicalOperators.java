public class LogicalOperators {

    public static void main(String[] args) {
        int a=5, b=6, c=8;
        //Logical operators
        System.out.println("Logical operators : ");
        System.out.println("AND: " + ((a == b) && (a == c)));
        System.out.println("OR: " + ((a == b) || (a == c)));
        System.out.println("NOT: " + !(a == b));
        System.out.println(" ");
    }
}
